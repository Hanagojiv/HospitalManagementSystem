    private void btn_update4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_update4ActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = Table4.getSelectedRow();
        if(selectedRowIndex <0)
        {
            JOptionPane.showMessageDialog(this,"Please select a row to view employee details");
           return;
        }
        
      DefaultTableModel model = (DefaultTableModel) Table4.getModel();
      Encounter enc = (Encounter) model.getValueAt(selectedRowIndex, 0);
      
        String PatientID = txtbx_ptid.getText();
        int HosID = Integer.parseInt(txtbx_hospid.getText());
        int DocID = Integer.parseInt(txtbx_docid.getText());
        int PulseRate = Integer.parseInt(txtbx_prate.getText());
        int BodyTemp = Integer.parseInt(txtbx_temp.getText());
        String ptName = txtbx_name.getText();
        String docName = txtbx_docname.getText();
        String hospName = txtbx_email2.getText();
        String bloodpress = txtbx_bpress.getText();
        
        enc.setPulseRate(PulseRate);
        enc.setTemprature(PulseRate);
        enc.setBloodPressure(bloodpress);
        
        enc.setDocID(DocID);
        enc.setDocName(docName);
        
        enc.setHospID(HosID);
        enc.setHospName(hospName);
        
        enc.setPatientID(PatientID);
        enc.setPatientName(ptName);
        
        populateTable();
        
        JOptionPane.showMessageDialog(this,"Encounter Updated");
        
    }//GEN-LAST:event_btn_update4ActionPerformed

