    private void btn_viewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_viewActionPerformed
        // TODO add your handling code here:
        
        int row = Table.getSelectedRow();

        if(row<0){
            JOptionPane.showMessageDialog(this, "Please select a ROW to search ");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) Table.getModel();
        Doctor selectedDoctor = (Doctor)model.getValueAt(row, 0);


        txtbx_id.setText(selectedDoctor.getDocID());
        txtbx_name.setText(selectedDoctor.getDocName());
        txtbx_contact.setText(selectedDoctor.getContact());
        txtbx_gender.setText(selectedDoctor.getGender());
        txtbx_age.setText(selectedDoctor.getAge());
        
    }//GEN-LAST:event_btn_viewActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
        int row = Table.getSelectedRow();

        if(row<0){
            JOptionPane.showMessageDialog(this, "Please select a ROW to search ");
            return;
        }
//
        DefaultTableModel model = (DefaultTableModel) Table.getModel();
        Doctor selectedDoctor = (Doctor)model.getValueAt(row, 0);
        
        String id = txtbx_id.getText();
        String name = txtbx_name.getText();
        String contact = txtbx_contact.getText();
        String gender = txtbx_gender.getText();
        String age = txtbx_age.getText();
        
        selectedDoctor.setDocID(id);
        selectedDoctor.setDocName(name);
        selectedDoctor.setContact(contact);
        selectedDoctor.setGender(gender);
        selectedDoctor.setAge(age);
        
        populateTable();
    }//GEN-LAST:event_btn_updateActionPerformed


