    private void populateTable(){
        
        DefaultTableModel model = (DefaultTableModel) Table.getModel();
        model.setRowCount(0);
        
        for(Doctor doc : docDir.getDoctorDirectory()){
            
            Object[] row = new Object[4];
            row[0] = doc;
            row[1] = doc.getDocName();
            row[2] = doc.getAge();
            row[3] = doc.getContact();
            
            model.addRow(row);
        }
    }

