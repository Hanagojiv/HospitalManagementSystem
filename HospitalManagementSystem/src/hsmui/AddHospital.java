    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        // TODO add your handling code here:
        
        
        
        String hsopId = txtbx_id.getText();
        String name = txtbx_name.getText();
        String city = txtbx_city.getSelectedItem().toString();
        String community = txtbx_community.getSelectedItem().toString();
         boolean valid = validationForFields();
         
         
         if(valid)
         {
        
        Hospital hosp = hospDirInfo.addNewHospital();
        
        hosp.setHospitalID(hsopId);
        hosp.setName(name);
        hosp.setCity(city);
        hosp.setCommunity(community);


        JOptionPane.showMessageDialog(this , "New Hospital Information Added!");
        
        txtbx_id.setText("");
        txtbx_name.setText("");
        
        txtbx_city.setName("select gender");
        txtbx_community.setName("select");

         }
    }//GEN-LAST:event_AddButtonActionPerformed
private boolean validationForFields() {
    
    
     if(txtbx_name.getText().length()==0)
                {
                     JOptionPane.showMessageDialog(this," Name Can't be blank, please enter Hospital Name");
                     txtbx_name.requestFocus();
                     txtbx_name.setText("");
                     return false;
                }
                
               else{
        if(!txtbx_name.getText().matches("^[a-zA-Z]+[\\-'\\s]?[a-zA-Z ]+$"))
            {
                 JOptionPane.showMessageDialog(this," Invalid name. ");
                 txtbx_name.requestFocus();
                 txtbx_name.setText("");
                 return false;
                
                
            }
          }
                   if(txtbx_id.getText().isBlank() )
    {
        JOptionPane.showMessageDialog(this," Hospital ID Can't be blank, please enter Hospital ID ");
        txtbx_id.requestFocus();
        txtbx_id.setText("");
        return false;
    }
        else
        {
            
             if(!txtbx_id.getText().matches("^[0-9]{0,3}$"))
             {
                JOptionPane.showMessageDialog(this," Invalid Hospital ID, Note : Hospital ID is of 3 digit  ");
                txtbx_id.requestFocus();
                return false;
                
             }  
        }
                       if(txtbx_city.getSelectedIndex() == 0)
            {
                    JOptionPane.showMessageDialog(this," Please select a city ");
                    txtbx_city.requestFocus();
                    //comboCity.setText("");
                   return false;
            }
           
                if(txtbx_community.getSelectedIndex() == 0)
            {
                    JOptionPane.showMessageDialog(this," Please select a Community ");
                    txtbx_community.requestFocus();
                    //comboCity.setText("");
                   return false;
            }       
                   
                   
                   return true;
}
